<script>
document.addEventListener('DOMContentLoaded', function() {
  const deviceBtns = document.querySelectorAll('.device-btn');
  const deviceContents = document.querySelectorAll('.device-content');
  
  deviceBtns.forEach(btn => {
    btn.addEventListener('click', function() {
      // Remove active class from all buttons
      deviceBtns.forEach(b => b.classList.remove('active'));
      
      // Add active class to clicked button
      this.classList.add('active');
      
      // Hide all content sections
      deviceContents.forEach(c => c.classList.remove('active'));
      
      // Show the selected content section
      const deviceToShow = this.getAttribute('data-device');
      document.getElementById(deviceToShow + '-content').classList.add('active');
    });
  });
});
</script>